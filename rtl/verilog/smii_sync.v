module smii_sync
  (
   // SMII sync
    output            sync,
   // internal
    output reg [1:10] state,
   // clock amd reset
    input 	      clk,
    input 	      rst
   );
   
   // sync shall go high every 10:th cycle
   always @ (posedge clk or posedge rst)
     if (rst)
       state <= 10'b0000000001;
     else
       state <= {state[10],state[1:9]};

   assign sync = state[1];
   
endmodule // smii_sync
