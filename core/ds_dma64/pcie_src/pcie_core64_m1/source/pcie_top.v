  always @(posedge user_clk or negedge clock_lock) begin

    if (clock_lock == 0) begin

      tx_state <= 2'b0;
      dst_rdy_ctr <= 4'b0;
      dst_rdy_trig <= 0;

    end else begin

      dst_rdy_trig <= 0;

      case (tx_state)

        2'b00: begin

          if ((llk_tx_sof_n == 0) && 
              (llk_tx_src_rdy_n == 0) && 
              (llk_tx_dst_rdy_n == 0)) begin

            dst_rdy_ctr <= 4'b0; // reset counter
            tx_state <= 2'b01;

          end else if ((llk_tx_sof_n == 0) && 
                       (llk_tx_src_rdy_n == 0) && 
                       (llk_tx_dst_rdy_n != 0)) begin

            if (dst_rdy_ctr == 5'b11111) begin 

              dst_rdy_trig <= 1'b1;
              tx_state <= 2'b10;

            end else begin

              dst_rdy_ctr <= dst_rdy_ctr + 1'b1;
              tx_state <= 2'b00;

            end

          end else if (llk_tx_dst_rdy_n != 0) begin

            if (dst_rdy_ctr == 5'b11111) begin 

              dst_rdy_trig <= 1'b1;
              tx_state <= 2'b10;

            end else begin

              dst_rdy_ctr <= dst_rdy_ctr + 1'b1;
              tx_state <= 2'b00;

            end
         
          end else begin

            dst_rdy_ctr <= 4'b0;
            tx_state <= 2'b00;

          end
